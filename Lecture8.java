public class Lecture8 {
    // Loop

    public static void main(String[] args) {
        int i = 0;
        for (i = 0; i < 100; i++) {
            System.out.println(i);

        }
        System.out.println("The value of i = " + i);
        // While loop

        int n = 5;
        while (n > 0) {
            System.out.println("Ritik");
            n--;

        }

        // do while loop
        int m = 5;
        do {
            System.out.println("Zooooom");
            m--;
        } while (m > 0);

        int k = 0;
        do {
            System.out.println("Hello");
            k--;
        } while (k > 0);

    }
}
